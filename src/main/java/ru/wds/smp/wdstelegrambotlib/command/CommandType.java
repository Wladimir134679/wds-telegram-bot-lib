package ru.wds.smp.wdstelegrambotlib.command;

/**
 * Тип команды — источник, из которого она поступает.
 *
 * <p>Разделение, как в старом проекте, но без значения {@code NONE}: команда либо
 * текстовая, либо из callback-кнопки. Один и тот же контроллер может содержать
 * методы обоих типов.</p>
 */
public enum CommandType {

    /** Команда из текстового сообщения (например, {@code /start}). */
    MESSAGE,

    /** Команда из нажатия inline-кнопки ({@code callback_data}). Слой будет добавлен следующим этапом. */
    CALLBACK
}
