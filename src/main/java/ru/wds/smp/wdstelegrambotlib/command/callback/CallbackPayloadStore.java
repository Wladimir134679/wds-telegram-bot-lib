package ru.wds.smp.wdstelegrambotlib.command.callback;

import java.util.Optional;

/**
 * Временное серверное хранилище «больших» данных для callback-кнопок.
 *
 * <p>Решает проблему лимита {@code callback_data} в 64 байта: разработчик кладёт
 * объёмные данные (набор правил, привилегий, ссылок на чаты, параметры выборки и
 * т.п.) в хранилище, получает короткий {@code payloadId}, и кладёт в кнопку только
 * этот ключ. При нажатии библиотека достаёт данные обратно по ключу.</p>
 *
 * <p><b>Жизненный цикл данных.</b> Записи живут ограниченное время (TTL) и
 * автоматически вычищаются по его истечении — это разовая «корзина» под конкретный
 * диалог, а не постоянное хранилище. Куда и что класть — решает разработчик; тип
 * полезной нагрузки произвольный.</p>
 *
 * <p>Реализация по умолчанию — {@link InMemoryCallbackPayloadStore}. Потребитель
 * может заменить её своим бином (например, на Redis) для распределённых сценариев.</p>
 *
 * <p>Реализации обязаны быть потокобезопасными.</p>
 */
public interface CallbackPayloadStore {

    /**
     * Сохраняет данные и возвращает короткий ключ-ссылку для {@code callback_data}.
     *
     * @param payload произвольные данные (не {@code null})
     * @return сгенерированный {@code payloadId}
     */
    String save(Object payload);

    /**
     * Возвращает данные по ключу, если они ещё живы.
     *
     * @param payloadId ключ-ссылка (может быть {@code null})
     * @return данные или {@link Optional#empty()}, если ключ не найден/истёк
     */
    Optional<Object> get(String payloadId);

    /**
     * Возвращает данные по ключу с приведением к ожидаемому типу.
     *
     * @param payloadId ключ-ссылка
     * @param type      ожидаемый тип данных
     * @param <T>       тип данных
     * @return данные нужного типа, либо {@link Optional#empty()}, если ключ
     *         истёк/не найден или тип не совпал
     */
    <T> Optional<T> get(String payloadId, Class<T> type);

    /**
     * Явно удаляет данные по ключу (например, после завершения диалога).
     *
     * @param payloadId ключ-ссылка
     */
    void remove(String payloadId);
}
