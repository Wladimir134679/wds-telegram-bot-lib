package ru.wds.smp.wdstelegrambotlib.handler;

import org.springframework.core.Ordered;
import org.telegram.telegrambots.meta.api.objects.Update;
import ru.wds.smp.wdstelegrambotlib.core.TelegramBotSender;

/**
 * Звено цепочки обработки входящих апдейтов Telegram.
 *
 * <p>Это ключевая точка расширения библиотеки. Каждый входящий {@link Update}
 * прогоняется через список обработчиков, отсортированных по приоритету
 * ({@link #getOrder()}). Новый этап обработки (логирование, метрики, анти-флуд,
 * маршрутизация команд) — это просто новый бин {@code UpdateHandler} в контексте
 * потребителя; Spring сам соберёт их все в общий список.</p>
 *
 * <p><b>Порядок.</b> Интерфейс наследует {@link Ordered}: чем меньше значение
 * {@link #getOrder()}, тем раньше вызывается обработчик. Готовые константы —
 * в {@link HandlerPriority}. По умолчанию используется
 * {@link HandlerPriority#DEFAULT}.</p>
 *
 * <p><b>Прерывание цепочки.</b> Метод {@link #handle(TelegramBotSender, Update)}
 * возвращает признак продолжения: {@code true} — передать апдейт следующему
 * обработчику, {@code false} — прервать цепочку (апдейт «поглощён», например
 * заблокирован анти-флудом или проверкой доступа).</p>
 *
 * <p><b>Изоляция ошибок.</b> Диспетчер оборачивает каждый вызов в try/catch:
 * исключение в одном обработчике не роняет обработку всего апдейта и не прерывает
 * цепочку для остальных. Тем не менее обработчик должен сам стремиться не падать.</p>
 *
 * <p><b>Потокобезопасность.</b> Транспорт long polling по умолчанию обрабатывает
 * апдейты в одном потоке, но обработчик не должен полагаться на это и не должен
 * хранить изменяемое состояние, специфичное для одного апдейта, в полях бина.</p>
 *
 * <p><i>Примечание:</i> сейчас в метод передаются {@link TelegramBotSender} (для
 * ответов) и сырой {@link Update}. Позже здесь появится общий контекст обработки
 * апдейта, через который обработчики смогут обмениваться данными.</p>
 */
public interface UpdateHandler extends Ordered {

    /**
     * Обрабатывает один входящий апдейт.
     *
     * @param sender абстракция отправки сообщений/выполнения методов Telegram API
     * @param update входящий апдейт от Telegram
     * @return {@code true} — продолжить цепочку (передать апдейт следующему
     *         обработчику); {@code false} — прервать дальнейшую обработку апдейта
     */
    boolean handle(TelegramBotSender sender, Update update);

    /**
     * Приоритет обработчика в цепочке. Меньше — раньше.
     *
     * @return порядковое значение; по умолчанию {@link HandlerPriority#DEFAULT}
     */
    @Override
    default int getOrder() {
        return HandlerPriority.DEFAULT;
    }
}
