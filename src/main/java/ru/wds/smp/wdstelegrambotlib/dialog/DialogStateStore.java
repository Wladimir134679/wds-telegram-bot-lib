package ru.wds.smp.wdstelegrambotlib.dialog;

import java.util.Optional;

/**
 * Хранилище активных диалоговых сессий с TTL.
 *
 * <p>Ключ — {@link DialogKey} (чат + пользователь). Реализация по умолчанию держит
 * состояния в памяти и фоново вычищает протухшие по истечении настраиваемого TTL
 * (см. {@code telegram.bot.dialog.ttl}). Это даёт диалогам контекст предыдущих
 * шагов без внешнего хранилища.</p>
 *
 * <p><b>Опциональность.</b> Когда слой диалогов выключен
 * ({@code telegram.bot.dialog.enabled=false}), в контексте присутствует no-op
 * реализация ({@link NoOpDialogStateStore}) — она ничего не хранит. Это убирает
 * россыпь null-проверок: код всегда может обратиться к стору, просто диалог никогда
 * не будет «активен».</p>
 *
 * <p><b>Потокобезопасность.</b> Все методы должны быть безопасны для конкурентного
 * вызова.</p>
 */
public interface DialogStateStore {

    /**
     * Возвращает активное (не протухшее) состояние диалога для ключа.
     *
     * @param key ключ сессии
     * @return состояние либо {@link Optional#empty()}, если диалога нет или он истёк
     */
    Optional<DialogState> find(DialogKey key);

    /**
     * Запускает новый диалог для ключа, заменяя любой ранее активный (новая команда
     * «побеждает»). Возвращает свежесозданное пустое состояние.
     *
     * @param key        ключ сессии
     * @param dialogName имя (ключ) запускаемого диалога
     * @return новое состояние диалога
     */
    DialogState start(DialogKey key, String dialogName);

    /**
     * Фиксирует изменения состояния и продлевает его жизнь (сбрасывает отсчёт TTL).
     * Вызывается после успешного шага, оставившего диалог активным.
     *
     * @param key   ключ сессии
     * @param state состояние для сохранения
     */
    void save(DialogKey key, DialogState state);

    /**
     * Удаляет диалог (по завершении, отмене или приходу новой команды).
     *
     * @param key ключ сессии
     */
    void remove(DialogKey key);
}
