package ru.wds.smp.wdstelegrambotlib.dialog;

import org.telegram.telegrambots.meta.api.methods.updatingmessages.EditMessageText;
import org.telegram.telegrambots.meta.api.objects.replykeyboard.InlineKeyboardMarkup;

import java.util.List;
import java.util.Optional;

/**
 * Контекст текущего шага диалога — главный публичный API диалоговой машины.
 *
 * <p>Инъектируется в методы {@code @DialogStart}/{@code @DialogStep} по типу (как
 * {@code CommandInvocation} в обычные команды). Через него шаг читает и пишет
 * накопленные данные, управляет переходами автомата и видит историю пройденных
 * шагов.</p>
 *
 * <p><b>Управление автоматом.</b> Один шаг должен завершиться ровно одним
 * «исходом»:</p>
 * <ul>
 *   <li>{@link #next(String)} — перейти на другой шаг (ждать следующего сообщения);</li>
 *   <li>{@link #finish()} — успешно завершить диалог (состояние очищается);</li>
 *   <li>{@link #cancel()} — отменить диалог (состояние очищается);</li>
 *   <li>ничего не вызвать — остаться на текущем шаге (например, при невалидном
 *       вводе попросить пользователя повторить).</li>
 * </ul>
 * Если в одном шаге вызвать несколько управляющих методов, применяется последний.
 *
 * <p><b>Накопленные данные.</b> {@link #set(String, Object)}/{@link #get(String)} —
 * это типобезопасная «корзина» значений, переживающая между шагами в пределах TTL
 * диалога. {@link #get(String)} использует вывод типа места присваивания —
 * приводите осознанно, кладите и читайте по одному ключу совместимые типы.</p>
 *
 * <p><b>Потокобезопасность.</b> Экземпляр относится к одному апдейту и не должен
 * сохраняться в полях бинов или передаваться между потоками.</p>
 */
public interface DialogContext {

    /** @return имя (ключ) текущего диалога */
    String dialog();

    /** @return имя текущего шага либо {@code null}, если шаг ещё не назначен */
    String step();

    /** @return идентификатор чата */
    long chatId();

    /** @return идентификатор пользователя */
    long userId();

    /**
     * Идентификатор «активного» сообщения для редактирования: для callback-обработчика
     * это сообщение, на котором нажата кнопка; для текстового шага — «якорное»
     * сообщение диалога (последнее отправленное меню/клавиатура).
     *
     * @return id сообщения либо {@code null}, если диалог ещё не отправлял сообщений
     */
    Integer messageId();

    /**
     * Готовит {@link EditMessageText} для редактирования «якорного» сообщения диалога
     * (см. {@link #messageId()}): подставляет {@code chatId} и {@code messageId}, чтобы
     * не дублировать это в каждом обработчике. Удобно для навигации по меню в одном
     * сообщении.
     *
     * @param text     новый текст сообщения
     * @param keyboard новая inline-клавиатура; {@code null} — не менять разметку
     *                 (клавиатура остаётся прежней), пустая разметка
     *                 ({@code Keyboards.inline().build()}) — убрать клавиатуру
     * @return метод редактирования, который можно вернуть из обработчика
     * @throws IllegalStateException если редактировать нечего ({@link #messageId()} == null)
     */
    EditMessageText edit(String text, InlineKeyboardMarkup keyboard);

    /**
     * Сокращение {@link #edit(String, InlineKeyboardMarkup)} без изменения клавиатуры.
     *
     * @param text новый текст
     * @return метод редактирования
     */
    EditMessageText edit(String text);

    /**
     * Кладёт значение в контекст диалога под ключом.
     *
     * @param key   ключ (не {@code null})
     * @param value значение; {@code null} удаляет ключ
     */
    void set(String key, Object value);

    /**
     * Возвращает ранее сохранённое значение.
     *
     * <p>Тип выводится из места присваивания и приводится без проверки — храните
     * под одним ключом совместимые типы. Для безопасного варианта см.
     * {@link #get(String, Class)}.</p>
     *
     * @param key ключ
     * @param <T> ожидаемый тип значения
     * @return значение либо {@code null}, если ключа нет
     */
    <T> T get(String key);

    /**
     * Возвращает значение, проверяя его тип.
     *
     * @param key  ключ
     * @param type ожидаемый класс значения
     * @param <T>  ожидаемый тип
     * @return значение нужного типа либо {@link Optional#empty()}
     */
    <T> Optional<T> get(String key, Class<T> type);

    /**
     * @param key ключ
     * @return {@code true}, если под ключом есть значение
     */
    boolean has(String key);

    /**
     * Удаляет значение по ключу.
     *
     * @param key ключ
     */
    void remove(String key);

    /**
     * Назначает следующий шаг автомата. Диалог остаётся активным; следующее
     * сообщение пользователя попадёт в метод этого шага.
     *
     * @param step имя шага (должно существовать в этом диалоге)
     */
    void next(String step);

    /**
     * Успешно завершает диалог. Состояние пользователя очищается; последующие
     * обычные сообщения снова считаются «вне диалога».
     */
    void finish();

    /**
     * Отменяет диалог. Семантически отличается от {@link #finish()} только
     * намерением; состояние так же очищается.
     */
    void cancel();

    /**
     * История пройденных шагов (от старых к новым), ограниченная по размеру. Удобно
     * для логики «назад» или диагностики сценария.
     *
     * @return неизменяемый список имён шагов
     */
    List<String> history();
}
